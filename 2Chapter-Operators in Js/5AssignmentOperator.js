//1.Arithmetic Operator (=, +=, -=, *=, /=, %=):
// = Assign.
// += Add Then Assign.
// -= Subtract Then Assign.
// *= Multiply Then Assign.
// /= Quotient Then Assign.
// %= Remainder Then Assign.

//1.Assign
let a = 20; //variable a me 20 value assign(yani daaldo) kardo
let b = a; //variable b me variable a ki value(jo ki 20 hai) usko assign kardo.
//Ab a bhi 20 and b bhi 20.
console.log(a); //20
console.log(b); //20

//2.Add then assign
a = a + 10;
//a = a + 10; means //a = 20 + 10;
// Means a me variable a ki value(jo ki 20 hai) usme 10 add karke a me assign kardo.
//Same chiz ko kuch ayese bhi likh sakte hai
a += 10;
//a = a + 10;
//a = 20 + 10;

//3. Subtract then assign
a = a - 10;
//a = a - 10; means //a = 20 - 10;
// Means a me variable a ki value(jo ki 20 hai) usme 10 sub karke a me assign kardo.
//Same chiz ko kuch ayese bhi likh sakte hai
a -= 10;
//a = a - 10;
//a = 20 - 10;

//4. Multipy then assign
a = a * 10;
//a = a * 10; means //a = 20 * 10;
// Means a me variable a ki value(jo ki 20 hai) usko 10 se multiply karke a me assign kardo.
//Same chiz ko kuch ayese bhi likh sakte hai
a *= 10;
//a = a * 10;
//a = 20 * 10;

//5. Quotient then assign
a = a / 10;
//a = a / 10; means //a = 20 + 10;
// Means a me variable a ki value(jo ki 20 hai) usko 10 se divide karke jo quotient bachega usko a me assign kardo.
//Same chiz ko kuch ayese bhi likh sakte hai
a /= 10;
//a = a / 10;
//a = 20 / 10;

//6. Remainder then assign
a = a % 10;
//a = a % 10; means //a = 20 % 10;
// Means a me variable a ki value(jo ki 20 hai) usko 10 se divide karke jo remainder bachega usko a me assign kardo.
//Same chiz ko kuch ayese bhi likh sakte hai
a %= 10;
//a = a % 10;
//a = 20 % 10;
